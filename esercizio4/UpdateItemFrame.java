    public UpdateItemFrame(InventoryDashboard inventoryDashboard) {
        initComponents();
        
        this.itemListModel = new DefaultListModel<String>();
        this.infoList.setModel(itemListModel);
        
        updateDisplay(inventoryDashboard);       
                
        saveButton.addActionListener(e ->{
            int index = Integer.parseInt(indexField.getText());
            String nome = nomeField.getText();
            int quantita = Integer.parseInt(quantityField.getText());
            double prezzo = Double.parseDouble(prezzoField.getText());
        
            inventoryDashboard.itemManager.getItem(index).setNome(nome);
            inventoryDashboard.itemManager.getItem(index).setQuantita(quantita);
            inventoryDashboard.itemManager.getItem(index).setPrezzo(prezzo);
        
            JOptionPane.showMessageDialog(this, "Oggetto aggiornato!");
            this.dispose();
        });
    }
    
    private void updateDisplay (InventoryDashboard inventoryDashboard) {
        for (int i = 0; i < 30; i++) {
            Item item = inventoryDashboard.itemManager.getItem(i);
            if (item != null) {
                String info = i + "  " + item.getNome() + "  " + item.getQuantita() + "  " + item.getPrezzo();
                itemListModel.addElement(info);
            }
        }
    }

