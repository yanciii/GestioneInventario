    public void addRowToJTable () {
        DefaultTableModel model = (DefaultTableModel) infoTable.getModel();
        model.setRowCount(0);
        Object rowData[] = new Object[4];
        
        for (int i = 0; i < 30; i++) {
            if (itemManager.getItem(i) != null) {
                rowData[0] = i + 1;
                rowData[1] = itemManager.getItem(i).getNome();
                rowData[2] = itemManager.getItem(i).getQuantita();
                rowData[3] = itemManager.getItem(i).getPrezzo();
                model.addRow(rowData);
            }            
        }
    }
